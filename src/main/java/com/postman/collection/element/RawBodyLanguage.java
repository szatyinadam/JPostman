package com.postman.collection.element;

/**
 * 
 * Enumerates valid values for the value of the <code>options/raw/language</code> property of a {@link RequestBody} object when that body's <code>mode</code> property
 * is set to <code>raw</code>
 * 
 * 
 */
public enum RawBodyLanguage {
    TEXT,JAVASCRIPT,JSON,HTML,XML,GRAPHQL;
}
